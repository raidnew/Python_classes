num = 132231

numString = str(num)

numStringLength = len(numString)

i = 0

while(i < numStringLength // 2):
    if(numString[i] != numString[-1 - i]):
        print("Данное четырехзначное число НЕ читается одинаково слева направо и справа налево")
        break
    i+=1
else:
    print("Данное четырехзначное число читается одинаково слева направо и справа налево")